        blueDice.setText(""+diceNum);
        if(bluePices[0]==0 && bluePices[1]==0 && bluePices[2]==0 && bluePices[3]==0){
        if(diceNum<6){
            blueDice.setEnabled(false);
            redDice.setEnabled(true);
            playerNumber=(playerNumber+1)%4;
            diceRolled=0;
        }
        }else{
            if(!can_move(bluePices)){
                blueDice.setEnabled(false);
            redDice.setEnabled(true);
            playerNumber=(playerNumber+1)%4;
            diceRolled=0;
            }
        }  // TODO add your handling code here:
